def heading(doc, text, level=1):
    p = doc.add_heading(text, level=level)
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    return p

def body(doc, text, bold=False, italic=False, indent=False):
    p = doc.add_paragraph()
    if indent:
        p.paragraph_format.left_indent = Cm(1)
    run = p.add_run(text)
    run.bold = bold
    run.italic = italic
    run.font.size = Pt(12)
    return p

def formula(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(2)
    run = p.add_run(text)
    run.italic = True
    run.font.size = Pt(12)
    return p

def add_table(doc, headers, rows, caption=''):
    if caption:
        c = doc.add_paragraph(caption)
        c.runs[0].bold = True
        c.runs[0].font.size = Pt(11)
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = 'Table Grid'
    hdr = t.rows[0]
    for i, h in enumerate(headers):
        hdr.cells[i].text = h
        hdr.cells[i].paragraphs[0].runs[0].bold = True
        hdr.cells[i].paragraphs[0].runs[0].font.size = Pt(10)
    for row in rows:
        r = t.add_row()
        for i, val in enumerate(row):
            r.cells[i].text = str(val)
            r.cells[i].paragraphs[0].runs[0].font.size = Pt(10)
    doc.add_paragraph()

